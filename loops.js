 var john = ['john', 'Mark', 1990, 'student', 'single'];

 // Using the for loop
 for (i = 0; i < john.length; i++) {
    console.log(john[i]);
 }

  // Using the while loop
 var i = 0;
 while (i < john.length) {
     console.log(john[i]);
     i++;
 }


